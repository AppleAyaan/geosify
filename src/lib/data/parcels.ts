/**
 * Mock parcel geometries are anchored to Waterloo industrial / greenfield
 * corridors (R&T Park, Ira Needles, Erbsville, Northfield) so overlays sit
 * on grass and vacant lots rather than downtown building footprints.
 */

function parcel(
  id: string,
  apn: string,
  address: string,
  lng: number,
  lat: number,
  width: number,
  height: number,
  acreage: number,
  price: number,
  zoning: string,
  zoningLabel: string,
  vacancyStatus: "vacant" | "improved" | "unknown",
  breakdown: {
    location: number;
    zoning: number;
    price: number;
    developmentPotential: number;
  },
  geometry?: GeoJSON.Polygon,
) {
  const investmentScore = Math.round(computeScore(breakdown));
  return {
    type: "Feature" as const,
    id,
    geometry: geometry ?? makeRect(lng, lat, width, height),
    properties: {
      id,
      apn,
      address,
      city: "Waterloo",
      province: "ON",
      acreage,
      price,
      pricePerAcre: Math.round(price / acreage),
      zoning,
      zoningLabel,
      vacancyStatus,
      investmentScore,
      scoreBreakdown: breakdown,
    },
  };
}

